"""TrustBench scenario dataset — 200 epistemic honesty benchmarks across 5 domains."""

from __future__ import annotations

from dataclasses import dataclass
from typing import Literal

__all__ = ["BenchScenario", "DOMAINS", "load_scenarios"]

# Domain constants
DOMAINS = (
    "factual",
    "reasoning",
    "ambiguous",
    "out_of_distribution",
    "adversarial",
)
Domain = Literal["factual", "reasoning", "ambiguous", "out_of_distribution", "adversarial"]
Difficulty = Literal["easy", "medium", "hard"]


@dataclass(frozen=True)
class BenchScenario:
    """A single epistemic honesty scenario for TrustBench evaluation.

    Attributes:
        id: Unique scenario identifier (e.g., 'factual_001')
        question: The question posed to the model
        domain: One of: factual, reasoning, ambiguous, out_of_distribution,
            adversarial
        expected_confidence_low: Lower bound of well-calibrated confidence
            (0.0-1.0)
        expected_confidence_high: Upper bound of well-calibrated confidence
            (0.0-1.0)
        ground_truth: The correct answer or uncertainty acknowledgment
        difficulty: easy, medium, or hard
    """

    id: str
    question: str
    domain: Domain
    expected_confidence_low: float
    expected_confidence_high: float
    ground_truth: str
    difficulty: Difficulty


