def load_scenarios() -> list[BenchScenario]:
    """Load all 200 TrustBench scenarios.

    Returns 200 scenarios across 5 domains:
    - factual (40): Verifiable facts with known answers
    - reasoning (40): Logic/math with deterministic ground truth
    - ambiguous (40): Genuinely uncertain questions
    - out_of_distribution (40): Questions outside typical training data
    - adversarial (40): Designed to elicit overconfidence

    Returns:
        List of 200 BenchScenario objects in deterministic order
    """
    return [
        BenchScenario(
            id=row[0],
            question=row[1],
            domain=row[2],  # type: ignore
            expected_confidence_low=row[3],
            expected_confidence_high=row[4],
            ground_truth=row[5],
            difficulty=row[6],  # type: ignore
        )
        for row in _SCENARIOS_DATA
    ]
